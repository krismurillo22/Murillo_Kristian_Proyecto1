    private void jButton5ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton5ActionPerformed
        int cantidadcuentas = menuinicio1.cuentas.CantidadCuentas();
        String useractual=log.useractual;
        
        String fechaHoraActual = LocalDateTime.now().format(DateTimeFormatter.ofPattern("dd/MM/yyyy HH:mm:ss"));
        if (cantidadcuentas >=2){
            String Users[] = menuinicio1.cuentas.obtenerUsers(useractual);
            String useroSeleccionado=(String)JOptionPane.showInputDialog(
                    this,
                    "Selecciona tu oponente:",
                    "Seleccionar Oponente",
                    JOptionPane.PLAIN_MESSAGE,
                    null,
                    Users,
                    Users[0]
            );
            useroponente=useroSeleccionado;

            String[] opciones = {"Villano", "Heroe"};
            String equipoSeleccionado = (String) JOptionPane.showInputDialog(
                null,
                "Selecciona tu equipo:",
                "Seleccionar Equipo",
                JOptionPane.PLAIN_MESSAGE,
                null,
                opciones,
                opciones[0]
            );
            userequipo=equipoSeleccionado;
            
            if (useroSeleccionado != null && equipoSeleccionado != null) {
                                
                if (userequipo.equals("Heroe")){
                    UsuarioContra useract= menuinicio1.cuentas.buscar(useractual);
                    UsuarioContra useropon= menuinicio1.cuentas.buscar(useroponente);
                            
                    Juego tablero = new Juego(menuinicio1, log, this, useract, useropon);
                    tablero.setVisible(true);
                    this.dispose();
                }else {
                    UsuarioContra useract= menuinicio1.cuentas.buscar(useractual);
                    UsuarioContra useropon= menuinicio1.cuentas.buscar(useroponente);
                            
                    Juego tablero = new Juego(menuinicio1, log, this, useropon, useract);
                    tablero.setVisible(true);
                    this.dispose();
                }
                
            }
        }else{
            JOptionPane.showMessageDialog(this, "No hay oponente contra el que jugar.\n Por favor, crear otra cuenta.");
        }
        
        
    }//GEN-LAST:event_jButton5ActionPerformed

