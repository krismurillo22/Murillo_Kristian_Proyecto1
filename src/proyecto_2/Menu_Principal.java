    private void jButton7ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton7ActionPerformed
        int result = JOptionPane.showConfirmDialog(null, "Esta seguro que quiere eliminar su cuenta?", "Eliminar Cuenta", JOptionPane.YES_NO_OPTION, JOptionPane.WARNING_MESSAGE);
        String user=log.useractual;
        String pass=log.passactual;
        if (result == JOptionPane.YES_OPTION){
            String contra = JOptionPane.showInputDialog (this, "Ingrese su contraseña: ", "Confirmacion",JOptionPane.INFORMATION_MESSAGE);
            if (contra != null) {
                if (contra.equals(pass)) {
                    boolean eliminar = menuinicio1.cuentas.elminarCuenta(user, pass);
                    if (eliminar) {
                        JOptionPane.showMessageDialog(this, "Cuenta eliminada exitosamente.");
                        this.dispose();
                        menuinicio1.setVisible(true);
                    }
                } else {
                    JOptionPane.showMessageDialog(this, "Contraseña incorrecta.");
                }
            } else {
                JOptionPane.showMessageDialog(this, "Operación cancelada.");
            }  
        }
    }//GEN-LAST:event_jButton7ActionPerformed

